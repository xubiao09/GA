function Fc=CalculateConSpdFuelRate(v)
%%
%计算恒定速度的燃油消耗率Fc(g/s),v为速度(m/s)
%%
M=1500;  %汽车质量
F=0.015; %地面行驶阻力系数
A=0.36;  %风阻系数
ALPHA=[0.590858024136993;0.0573372137218396;0.000139191515258181]; %多项式油耗系数
%%
B=M*9.8*F; %地面行驶阻力（N）
a=0;
P=v.*(M*a+A*v.^2+B)/1000;  %发动机功率（kw）
Fc=ALPHA(1)+ALPHA(2)*P+ALPHA(3)*P.^2;